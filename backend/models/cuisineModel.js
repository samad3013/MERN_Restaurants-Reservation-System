const mongoose = require('mongoose');

const cuisineSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    imageUrl: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Cuisine = mongoose.model('Cuisine', cuisineSchema);

module.exports = Cuisine;